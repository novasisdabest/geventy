"use client";

import { useState } from "react";
import { signInWithOAuthAction } from "@/app/actions/auth";

type Provider = "google" | "apple";

export default function OAuthButtons() {
  const [loadingProvider, setLoadingProvider] = useState<Provider | null>(null);

  async function handleOAuth(provider: Provider) {
    setLoadingProvider(provider);
    const result = await signInWithOAuthAction(provider);

    if ("error" in result) {
      setLoadingProvider(null);
      // Surface error via alert â€” keeps the component self-contained
      alert(result.error);
      return;
    }

    window.location.href = result.url;
  }

  return (
    <div className="space-y-4">
      <div className="space-y-3">
        <button
          type="button"
          disabled={loadingProvider !== null}
          onClick={() => handleOAuth("google")}
          className="w-full py-3 px-4 border border-white/20 hover:border-white/40 disabled:border-slate-800 disabled:text-slate-600 text-white rounded-xl font-bold text-sm transition-colors flex items-center justify-center gap-3"
        >
          {loadingProvider === "google" ? (
            <Spinner />
          ) : (
            <GoogleIcon />
          )}
          Pokracovat pres Google
        </button>

        <button
          type="button"
          disabled={loadingProvider !== null}
          onClick={() => handleOAuth("apple")}
          className="w-full py-3 px-4 border border-slate-700 hover:border-slate-500 disabled:border-slate-800 disabled:text-slate-600 text-white rounded-xl font-bold text-sm transition-colors flex items-center justify-center gap-3"
        >
          {loadingProvider === "apple" ? (
            <Spinner />
          ) : (
            <AppleIcon />
          )}
          Pokracovat pres Apple
        </button>
      </div>

      {/* "nebo" divider */}
      <div className="flex items-center gap-4">
        <div className="flex-1 h-px bg-slate-800" />
        <span className="text-xs font-bold uppercase tracking-wider text-slate-600">
          nebo
        </span>
        <div className="flex-1 h-px bg-slate-800" />
      </div>
    </div>
  );
}

function Spinner() {
  return (
    <svg
      className="animate-spin h-5 w-5 text-slate-500"
      xmlns="http://www.w3.org/2000/svg"
      fill="none"
      viewBox="0 0 24 24"
    >
      <circle
        className="opacity-25"
        cx="12"
        cy="12"
        r="10"
        stroke="currentColor"
        strokeWidth="4"
      />
      <path
        className="opacity-75"
        fill="currentColor"
        d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4z"
      />
    </svg>
  );
}

function GoogleIcon() {
  return (
    <svg width="20" height="20" viewBox="0 0 24 24">
      <path
        d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92a5.06 5.06 0 01-2.2 3.32v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.1z"
        fill="#4285F4"
      />
      <path
        d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
        fill="#34A853"
      />
      <path
        d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
        fill="#FBBC05"
      />
      <path
        d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
        fill="#EA4335"
      />
    </svg>
  );
}

function AppleIcon() {
  return (
    <svg width="20" height="20" viewBox="0 0 24 24" fill="currentColor">
      <path d="M17.05 20.28c-.98.95-2.05.88-3.08.4-1.09-.5-2.08-.48-3.24 0-1.44.62-2.2.44-3.06-.4C2.79 15.25 3.51 7.59 9.05 7.31c1.35.07 2.29.74 3.08.8 1.18-.24 2.31-.93 3.57-.84 1.51.12 2.65.72 3.4 1.8-3.12 1.87-2.38 5.98.48 7.13-.57 1.5-1.31 2.99-2.54 4.09zM12.03 7.25c-.15-2.23 1.66-4.07 3.74-4.25.29 2.58-2.34 4.5-3.74 4.25z" />
    </svg>
  );
}
